function [] = f2(v,n)
    hold on
    A_1 = [0 0; 0 0.16];
    A_2 = [0.85 -0.04; 0.04 0.85];
    A_3 = [0.2 -0.26; 0.23 0.22];
    A_4 = [-0.15 0.28; 0.26 0.24];
    b_1 = [0; 0];
    b_2 = [0; 1.6];
    b_3 = [0; 1.6];
    b_4 = [0; 0.44];  
    r = rand();
    if (r < 0.01)
        v = A_1*v + b_1; 
    elseif (r < 0.86)
        v = A_2*v + b_2;
    elseif (r < 0.93)
        v = A_3*v + b_3;
    else
        v = A_4*v + b_4;
    end
    
    plot(v(1), v(2),'.')
    if (n > 0)
        f2(v, n-1)
    end
end